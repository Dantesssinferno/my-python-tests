# Напишите код, который принимает на вход три числа, это длины сторон треугольника.
# Выведите на печать тип треугольника.
# Используйте для ввода строчки:
#  a = int(input())
# b = int(input())
# c = int(input())
# Если все три числа равны - выводим на печать "Равносторонний треугольник"
# Если все два из трех чисел равны - выводим на печать "Равнобедренный треугольник"
# Если сумма двух чисел равна третьему - выводим на печать "Прямоугольный треугольник"
# (Фактически у прямоугольного треугольника, квадрат гипотенузу равен сумме квадратов катета,
# но для упрощения вычисления возьмём без квадратов)
# Если ни одно из условий выше не подходит - выводим на печать "Разносторонний треугольник"

# Создаю класс для собственных исключений для треугольника
class TriangleError(Exception):
    pass


# Считываем ввод пользователя
try:
    a = float(input())
    b = float(input())
    c = float(input())

    # 1. Проверяем "упрощённый прямоугольный"
    if a + b == c or a + c == b or b + c == a:
        print("Прямоугольный треугольник")

    # 2. Проверяем обычное неравенство треугольника
    elif a + b <= c or a + c <= b or b + c <= a:
        raise TriangleError("Треугольник не существует")

    # 3. Классификация
    elif a == b == c:
        print("Равносторонний треугольник")
    elif a == b or b == c or a == c:
        print("Равнобедренный треугольник")
    else:
        print("Разносторонний треугольник")

except TriangleError as e:
    print(e)

except ValueError:
    print("Некорректный ввод: ожидаются числа")

